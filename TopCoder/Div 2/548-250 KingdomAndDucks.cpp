class KingdomAndDucks {
public:
	int minDucks(vector <int> duckTypes) {
        int cnt[100]={0};
        int c=0,mx=0;
        rep(i,sz(duckTypes)){
            int idx = duckTypes[i];
            cnt[idx] ++;
            if(cnt[idx] == 1)
                ++c;
            mx = max(mx, cnt[idx]);
        }
        return c*mx;
	}
};


